## Calcular el sueldo de una persona, conociendo la cantidad de horas que trabaja en el
##mes y el valor de la hora.

horas = int(input("Ingrese la cantidad de horas que trabaja: "))
valor_hora = int(input("Ingrese el valor de la hora: "))

sueldo  = horas * valor_hora

print(f"Tu sueldo es: {sueldo}")

